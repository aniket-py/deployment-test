const CONTRACT = '0x1ada3E8E652a70C214A38aBe31353dE62DFBaEbC'

const provider = new ethers.providers.JsonRpcProvider("https://dapps.shardeum.org");

const erc721Contract = new ethers.Contract(CONTRACT, erc721Abi, provider)
let startBlock = 64957

const erc721Owner = async () => {
    const transfer = await erc721Contract.filters.Transfer().getTopicFilter()
    const topicFilter = [
        transfer
    ]
    const currentBlock = await provider.getBlockNumber()
    let nextBlock = currentBlock
    if (currentBlock - 1000 > startBlock) {
        nextBlock = startBlock + 1000
    }

    const events = await erc721Contract.queryFilter(topicFilter, startBlock, nextBlock)
    startBlock = nextBlock
    console.log('nextBlock', nextBlock)
    console.log('events', events)
}

const erc721OwnerJob = async () => {
    while (true) {
        try {
            const startJob = performance.now()
            await erc721Owner()
            const endJob = performance.now()

            if (endJob - startJob < 1000) {
                await delay(1000 - (endJob - startJob))
            }
        } catch (error) {
            console.log('erc721OwnerJob', error)
        }
    }
}

erc721OwnerJob()